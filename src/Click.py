import pygame

class Click:
    def __init__(self):
        self.pozycja = None
        self.positions = {
            "A1": ((84, 84 + 172), (46, 46 + 172)),
            "A2": ((84, 84 + 172), (46 + 172 + 25, 46 + 172 * 2 + 25)),
            "A3": ((84, 84 + 172), (46 + 172 * 2 + 25 * 2, 46 + 172 * 3 + 25 * 2)),
            "B1": ((84 + 172 + 25, 84 + 172 * 2 + 25), (46, 46 + 172)),
            "B2": ((84 + 172 + 25, 84 + 172 * 2 + 25), (46 + 172 + 25, 46 + 172 * 2 + 25)),
            "B3": ((84 + 172 + 25, 84 + 172 * 2 + 25), (46 + 172 * 2 + 25 * 2, 46 + 172 * 3 + 25 * 2)),
            "C1": ((84 + 172 * 2 + 25 * 2, 84 + 172 * 3 + 25 * 2), (46, 46 + 172)),
            "C2": ((84 + 172 * 2 + 25 * 2, 84 + 172 * 3 + 25 * 2), (46 + 172 + 25, 46 + 172 * 2 + 25)),
            "C3": ((84 + 172 * 2 + 25 * 2, 84 + 172 * 3 + 25 * 2), (46 + 172 * 2 + 25 * 2, 46 + 172 * 3 + 25 * 2)),
        }
    def get_position(self):
        cursor = pygame.mouse.get_pos()
        for position, ranges in self.positions.items():
            if ranges[0][0] < cursor[0] < ranges[0][1] and ranges[1][0] < cursor[1] < ranges[1][1]:
                self.pozycja = position
